import asyncio
import random

import websockets


async def tester():
    async with websockets.connect('ws://localhost:4317/poll') as websocket:

        while True:
            greeting = await websocket.recv()
            print("< {}".format(greeting))
            import time
            time.sleep(random.random() * 3)

asyncio.get_event_loop().run_until_complete(tester())
